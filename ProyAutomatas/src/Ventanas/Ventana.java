    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String datos = jTextField1.getText();

// Validar que no est√© vac√≠o
if (datos.trim().isEmpty()) {
    jTextArea1.setText("‚ùå ERROR: Por favor ingrese una expresi√≥n para analizar.");
    return;
}

try {
    // Crear instancias del analizador
    Analizador_Lexico lexico = new Analizador_Lexico(new BufferedReader(new StringReader(datos)));
    analisis_sintactico sintactico = new analisis_sintactico(lexico);
    
    // Limpiar tablas de errores previos (tanto l√©xicos como sint√°cticos)
    Analizador_Lexico.TablaEL.clear();
    analisis_sintactico.TablaES.clear();
    
    // Realizar el an√°lisis sint√°ctico (que internamente usa el l√©xico)
    sintactico.parse();
    
    // Verificar si hay errores l√©xicos
    boolean hayErroresLexicos = !Analizador_Lexico.TablaEL.isEmpty();
    boolean hayErroresSintacticos = !analisis_sintactico.TablaES.isEmpty();
    
    if (hayErroresLexicos || hayErroresSintacticos) {
        StringBuilder resultado = new StringBuilder();
        resultado.append("‚ùå ERRORES DETECTADOS EN EL AN√ÅLISIS\n\n");
        
        // Mostrar errores l√©xicos si los hay
        if (hayErroresLexicos) {
            resultado.append("üî§ ERRORES L√âXICOS:\n");
            resultado.append(String.format("%-10s %-10s %-15s %s\n", "L√çNEA", "COLUMNA", "LEXEMA", "DESCRIPCI√ìN"));
            resultado.append("---------------------------------------------------------------\n");
            
            for (TError error : Analizador_Lexico.TablaEL) {
                resultado.append(String.format("%-10d %-10d %-15s %s\n",
                        error.linea + 1, // +1 porque JFlex cuenta desde 0
                        error.columna + 1, // +1 porque JFlex cuenta desde 0
                        error.lexema.length() > 14 ? error.lexema.substring(0, 14) : error.lexema,
                        error.descripcion));
            }
            resultado.append("\n");
        }
        
        // Mostrar errores sint√°cticos si los hay
        if (hayErroresSintacticos) {
            resultado.append("üîß ERRORES SINT√ÅCTICOS:\n");
            resultado.append(String.format("%-10s %-10s %-15s %s\n", "L√çNEA", "COLUMNA", "LEXEMA", "DESCRIPCI√ìN"));
            resultado.append("---------------------------------------------------------------\n");
            
            for (TError error : analisis_sintactico.TablaES) {
                resultado.append(String.format("%-10d %-10d %-15s %s\n",
                        error.linea + 1, // +1 porque CUP cuenta desde 0
                        error.columna + 1, // +1 porque CUP cuenta desde 0
                        error.lexema.length() > 14 ? error.lexema.substring(0, 14) : error.lexema,
                        error.descripcion));
            }
            resultado.append("\n");
        }
        
        // Agregar resumen de errores
        resultado.append("üìä RESUMEN DE ERRORES:\n");
        if (hayErroresLexicos) {
            resultado.append("‚Ä¢ Errores l√©xicos: ").append(Analizador_Lexico.TablaEL.size()).append("\n");
        }
        if (hayErroresSintacticos) {
            resultado.append("‚Ä¢ Errores sint√°cticos: ").append(analisis_sintactico.TablaES.size()).append("\n");
        }
        resultado.append("‚Ä¢ Total de errores: ").append(
            Analizador_Lexico.TablaEL.size() + analisis_sintactico.TablaES.size());
        
        jTextArea1.setText(resultado.toString());
        
    } else {
        // An√°lisis completamente exitoso (sin errores l√©xicos ni sint√°cticos)
        String resultadoFinal = "‚úÖ AN√ÅLISIS L√âXICO Y SINT√ÅCTICO EXITOSO\n\n" +
                "üìù Expresi√≥n analizada: " + datos + "\n\n" +
                "üîç RESULTADOS DEL AN√ÅLISIS:\n" +
                "‚Ä¢ An√°lisis l√©xico: ‚úì Sin errores\n" +
                "‚Ä¢ An√°lisis sint√°ctico: ‚úì Sin errores\n" +
                "‚Ä¢ Estructura gramatical: ‚úì V√°lida\n\n" +
                "üìã INTERPRETACI√ìN:\n" +
                sintactico.resultado + "\n\n" +
                "‚ú® El c√≥digo ingresado cumple con las reglas l√©xicas y sint√°cticas definidas.\n\n" +
                "üí° TOKENS RECONOCIDOS:\n" +
                "La expresi√≥n fue tokenizada y parseada correctamente seg√∫n la gram√°tica de Visual Basic.";
        
        jTextArea1.setText(resultadoFinal);
    }
    
} catch (Exception e) {
    // Manejo de errores cr√≠ticos durante el an√°lisis
    StringBuilder mensajeError = new StringBuilder();
    mensajeError.append("‚ùå ERROR CR√çTICO EN EL AN√ÅLISIS\n\n");
    mensajeError.append("No fue posible completar el an√°lisis l√©xico-sint√°ctico.\n\n");
    mensajeError.append("üìù Expresi√≥n ingresada: ").append(datos).append("\n\n");
    
    // Verificar si hay errores l√©xicos o sint√°cticos parciales
    if (!Analizador_Lexico.TablaEL.isEmpty()) {
        mensajeError.append("‚ö†Ô∏è Se detectaron ").append(Analizador_Lexico.TablaEL.size())
                   .append(" errores l√©xicos antes del fallo.\n");
    }
    if (!analisis_sintactico.TablaES.isEmpty()) {
        mensajeError.append("‚ö†Ô∏è Se detectaron ").append(analisis_sintactico.TablaES.size())
                   .append(" errores sint√°cticos antes del fallo.\n");
    }
    
    mensajeError.append("\nüîç EJEMPLOS DE C√ìDIGO V√ÅLIDO:\n");
    mensajeError.append("‚Ä¢ Declaraci√≥n: DIM x INTEGER;\n");
    mensajeError.append("‚Ä¢ Asignaci√≥n: x = 10 + 5;\n");
    mensajeError.append("‚Ä¢ Condicional: IF x > 5 THEN x = 1; END IF\n");
    mensajeError.append("‚Ä¢ Ciclo: FOR i = 1 > 10 x = x + 1; NEXT i\n\n");
    
    mensajeError.append("üö® Detalle t√©cnico del error:\n");
    mensajeError.append(e.getMessage() != null ? e.getMessage() : "Error no especificado");
    mensajeError.append("\n\nüí° Verifique que el c√≥digo siga la sintaxis de Visual Basic.");
    
    jTextArea1.setText(mensajeError.toString());
    
    // Para debugging en desarrollo
    e.printStackTrace();
}
    }//GEN-LAST:event_jButton1ActionPerformed

